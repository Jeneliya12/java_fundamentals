package ifelsequestion;

public class NumberDivisibleByThree {

	public static void main(String[] args) {
		int num1 = 9;
		
	if (num1 % 3 == 0) {
		System.out.println("The number " + num1 + "is divisible by 3.");
	}
	else {
		System.out.println("The number" + num1 + " is not divisible by 3.");
	}

	}
}
