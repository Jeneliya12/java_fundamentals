package ifelsequestion;

public class NumberDivisibleByFive {

	public static void main(String[] args) {
		int num1 = 23;
		
		if (num1 % 5 == 0) {
			System.out.println("The number " + num1 + "is divisible by 5.");
		}
		else {
			System.out.println("The number" + num1 + " is not divisible by 5.");
		}

	}

}
